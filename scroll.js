


		document.getElementById("manan-menu").addEventListener("click", function(){
			console.log('manan++');
			var element = document.getElementById("MANANM");
			starty = pageYOffset;
			endy = element.offsetTop;

			if ((starty-endy)<0) {
				var x = setInterval(function(){
 				window.scrollBy(0, 3);
 				if (endy<=pageYOffset) 
 					clearInterval(x);
				}, 10);			
			}
			else{
				var x = setInterval(function(){
 				window.scrollBy(0, -3);
 				if (endy>=pageYOffset) 
 					clearInterval(x);
				}, 10);	
			}
		});


